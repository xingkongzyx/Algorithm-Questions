/**
 * 336. Palindrome Pairs
 *
 * 问题描述：
 * 给定一个单词列表 words，找出所有满足条件的下标对 (i, j)，
 * 使得 words[i] + words[j] 拼接后是一个回文串。
 *
 * ============================================================================
 * 算法核心思路
 * ============================================================================
 *
 * 【问题转化】
 * 暴力解法：遍历所有 (i, j) 对，检查拼接是否为回文 → O(n² * k)，太慢！
 * 优化思路：利用回文的对称性质，将问题转化为【查找】而非【验证】
 *
 * 【关键洞察】
 * 回文串的本质是"左右对称"，如果两个单词 A + B 能组成回文：
 *
 *   情况1：A 和 B 完全互为反转
 *          例：A = "abc", B = "cba" → "abccba" ✓
 *
 *   情况2：A 包含 B 的反转，且 A 的剩余部分自身是回文
 *          例：A = "aab", B = "a" 的反转是 "a"
 *              A 分割为 left="a" + right="ab"
 *              但这不行...
 *
 *          正确例子：A = "lls", B = "s"
 *              A 分割为 left="ll" + right="s"
 *              - left="ll" 是回文 ✓
 *              - reverse(right)="s" 存在于列表中 ✓
 *              - 拼接：B + A = "s" + "lls" = "slls" ✓
 *
 * 【核心公式】
 * 对于任意单词 curWord，将其分割为 left + right：
 *
 *   ┌──────────────────────────────────────────────────────────────────────┐
 *   │  如果 left 是回文，且存在单词 X 使得 X = reverse(right)              │
 *   │  那么：X + curWord = reverse(right) + left + right 是回文            │
 *   │                      ↑              ↑↑↑↑↑↑        ↑                  │
 *   │                      └── 两端互为镜像 ──┘  └─ 中间是回文 ─┘            │
 *   └──────────────────────────────────────────────────────────────────────┘
 *
 *   ┌──────────────────────────────────────────────────────────────────────┐
 *   │  如果 right 是回文，且存在单词 Y 使得 Y = reverse(left)              │
 *   │  那么：curWord + Y = left + right + reverse(left) 是回文             │
 *   │                      ↑       ↑↑↑↑↑↑            ↑                     │
 *   │                      └─ 中间是回文 ──┘  └── 两端互为镜像 ─┘            │
 *   └──────────────────────────────────────────────────────────────────────┘
 *
 * 【为什么存反转单词】
 * 我们需要快速查找"是否存在某个单词，它的反转等于 X"。
 * 如果预先存储每个单词的反转，就可以用 O(1) 时间直接查找！
 *
 *   预处理：reverseds = { 单词的反转 → 原始索引 }
 *   查找时：reverseds.has("abc") 就是在问"有没有单词反转后是 abc？"
 *
 * 【整体算法流程】
 *
 *   ┌─────────────────────────────────────────────────────────────────────┐
 *   │  Step 1: 预处理 - 构建反转单词的 Map                                  │
 *   │          { 单词的反转 → 原始索引 }                                    │
 *   └─────────────────────────────────────────────────────────────────────┘
 *                                    ↓
 *   ┌─────────────────────────────────────────────────────────────────────┐
 *   │  Step 2: 遍历每个单词 curWord                                        │
 *   │          - 特殊情况：curWord 是回文且存在空字符串 → 可配对            │
 *   └─────────────────────────────────────────────────────────────────────┘
 *                                    ↓
 *   ┌─────────────────────────────────────────────────────────────────────┐
 *   │  Step 3: 将 curWord 在每个位置分割成 left + right                    │
 *   │          - 若 left 是回文，找 reverse(right) → [reverse(right), i]  │
 *   │          - 若 right 是回文，找 reverse(left) → [i, reverse(left)]   │
 *   └─────────────────────────────────────────────────────────────────────┘
 *
 * ============================================================================
 * 分割单词寻找配对 - 详细讲解
 * ============================================================================
 *
 * 把 curWord 在每个位置切成两半：
 *
 *   curWord = "abcde"
 *   j=0:  left=""      right="abcde"
 *   j=1:  left="a"     right="bcde"
 *   j=2:  left="ab"    right="cde"
 *   j=3:  left="abc"   right="de"
 *   j=4:  left="abcd"  right="e"
 *
 * ----------------------------------------------------------------------------
 * 模式一：别的单词放前面 [other, curWord]
 * ----------------------------------------------------------------------------
 *
 * 当 left 是回文时，寻找 reverse(right)
 *
 *         other          curWord
 *     ┌──────────┐   ┌─────────────┐
 *     │reverse(R)│ + │  L   │  R   │
 *     └──────────┘   └─────────────┘
 *          ↑                   ↑
 *          └───── 互为镜像 ─────┘
 *                (抵消掉了)
 *
 *     中间的 L 必须是回文，才能保证整体是回文
 *
 * 例子：
 *   curWord = "lls", 分割 j=2: left="ll", right="s"
 *   left="ll" 是回文 ✓
 *   找 reverse("s") = "s" 是否在列表中
 *   如果 "s" 在列表中（假设索引为 k）
 *   拼接结果："s" + "lls" = "slls" ✓ 是回文！
 *   返回 [k, i]
 *
 * ----------------------------------------------------------------------------
 * 模式二：别的单词放后面 [curWord, other]
 * ----------------------------------------------------------------------------
 *
 * 当 right 是回文时，寻找 reverse(left)
 *
 *        curWord           other
 *     ┌─────────────┐   ┌──────────┐
 *     │  L   │  R   │ + │reverse(L)│
 *     └─────────────┘   └──────────┘
 *          ↑                   ↑
 *          └───── 互为镜像 ─────┘
 *                (抵消掉了)
 *
 *     中间的 R 必须是回文，才能保证整体是回文
 *
 * 例子：
 *   curWord = "sll", 分割 j=1: left="s", right="ll"
 *   right="ll" 是回文 ✓
 *   找 reverse("s") = "s" 是否在列表中
 *   如果 "s" 在列表中（假设索引为 k）
 *   拼接结果："sll" + "s" = "slls" ✓ 是回文！
 *   返回 [i, k]
 *
 * ============================================================================
 * 为什么存的是反转后的单词？
 * ============================================================================
 *
 * Map 里存的是：{ 反转后的单词 → 原始索引 }
 *
 * 当我们检查 reverseds.has(right) 时：
 *   - 实际在问：是否存在某个单词，它的反转等于 right？
 *   - 等价于：是否存在 reverse(right)？
 *
 * 这样就能直接用 right 作为 key 查找！
 *
 * 例：
 *   预处理时 words = ["bat", "tab"]
 *   reverseds = { "tab": 0, "bat": 1 }  // 存反转
 *
 *   查找时 curWord = "bat", right = "bat"
 *   reverseds.has("bat")  // 问的是：有没有单词反转后="bat"？
 *   答案：有！"tab" 反转后是 "bat"，索引是 1
 *
 * ============================================================================
 * 完整例子演示
 * ============================================================================
 *
 * words = ["abcd", "dcba", "lls", "s", "sssll"]
 *
 * 构建 Map（存反转）：
 *   {
 *     "dcba": 0,  // "abcd" 反转
 *     "abcd": 1,  // "dcba" 反转
 *     "sll": 2,   // "lls" 反转
 *     "s": 3,     // "s" 反转
 *     "llsss": 4  // "sssll" 反转
 *   }
 *
 * 处理 "lls" (i=2)：
 *   j=0: left=""   right="lls"  → left是回文, has("lls")? ×
 *   j=1: left="l"  right="ls"   → left是回文, has("ls")? ×
 *   j=2: left="ll" right="s"    → left是回文, has("s")? ✓ 索引3
 *                                → 添加 [3, 2] 即 "s"+"lls"="slls" ✓
 *
 * 处理 "sssll" (i=4)：
 *   j=2: left="ss" right="sll"  → has("sll")? ✓ 索引2
 *                                → right是回文? "sll" 不是
 *                                → left是回文? "ss" 是 ✓
 *                                → 添加 [2, 4] 即 "lls"+"sssll"="llssssll" ✓
 *
 * ============================================================================
 * 为什么需要 !== i 的判断？（避免自身配对）
 * ============================================================================
 *
 * 这是为了避免单词与自身配对。
 *
 * 问题场景：考虑一个回文单词，比如 "aba"
 *
 *   words = ["aba", "xyz"]
 *
 *   构建 Map（存反转）：
 *   reverseds = {
 *     "aba": 0,  // "aba" 反转后还是 "aba"
 *     "zyx": 1
 *   }
 *
 * 当处理 "aba" (i=0) 时，分割 j=0:
 *   left = ""
 *   right = "aba"
 *
 * 检查条件：
 *   isPalindrome(left) → "" 是回文 ✓
 *   reverseds.has(right) → reverseds.has("aba") ✓ 存在，索引是 0
 *
 * 如果没有 !== i 判断，会添加 [0, 0]，意味着 "aba" + "aba"。
 * 但题目要求的是不同下标的单词对 (i, j) 且 i ≠ j！
 *
 * 总结：
 *   | 情况      | 单词    | 反转    | 会发生什么                    |
 *   |----------|--------|--------|------------------------------|
 *   | 回文词    | "aba"  | "aba"  | 自己匹配自己，需要排除         |
 *   | 非回文词  | "abc"  | "cba"  | 不会匹配自己，但检查也无妨     |
 *
 * !== i 判断确保返回的下标对中，两个下标指向的是不同的单词，符合题目要求。
 *
 * ============================================================================
 * 复杂度分析
 * ============================================================================
 *
 * 时间复杂度：O(n * k²)
 *   - n 是单词数量，k 是平均单词长度
 *   - 每个单词有 k 个分割点，每次判断回文需要 O(k)
 *
 * 空间复杂度：O(n * k)
 *   - 用于存储反转单词的哈希表
 */

/**
 * 判断字符串是否为回文串
 * 双指针法，时间复杂度 O(n)
 */
const isPalindrome = (str) => {
    let l = 0,
        r = str.length - 1;
    while (l < r) {
        if (str[l++] != str[r--]) return false;
    }
    return true;
};
/**
 * 主函数：找出所有能组成回文的单词对
 * @param {string[]} words - 单词列表
 * @return {number[][]} - 所有满足条件的下标对 [i, j]
 */
const palindromePairs = (words) => {
    // Step 1: 构建反转单词的哈希表
    // key: 单词的反转, value: 原单词的索引
    // 这样查找时可以用原字符串直接作为 key
    const reverseds = new Map();
    for (let i = 0; i < words.length; i++) {
        const reversed = words[i].split("").reverse().join("");
        reverseds.set(reversed, i);
    }

    const res = [];

    for (let i = 0; i < words.length; i++) {
        const curWord = words[i];

        // Step 2: 特殊情况 - 空字符串配对
        //
        // 【为什么需要单独处理？】
        // 当 curWord 是回文词（如 "aba"），且列表中存在空字符串 "" 时：
        //   - "" + "aba" = "aba" 是回文 ✓
        //   - "aba" + "" = "aba" 是回文 ✓
        //
        // 【为什么 Step 3 的分割逻辑无法处理？】
        // Step 3 在 j=0 时：left=""，right=curWord
        //   - 检查 reverseds.has(curWord) 找的是 curWord 的反转词
        //   - 不会去找空字符串 ""！
        //
        // 例：words = ["aba", ""]
        //   reverseds = { "aba": 0, "": 1 }
        //
        //   处理 "aba"，j=0：
        //     - left="", right="aba"
        //     - reverseds.has("aba") → 找到自己(索引0)，被 !== i 排除
        //     - 永远不会检查 reverseds.has("")！
        //
        // 所以必须单独处理：如果 curWord 是回文，检查是否存在空字符串
        // 注：curWord + "" 会在遍历到空字符串时被处理
        if (
            isPalindrome(curWord) &&
            reverseds.has("") &&
            curWord !== ""
        ) {
            res.push([reverseds.get(""), i]);
        }

        // Step 3: 核心逻辑 - 分割单词寻找配对
        // 将当前单词按每个位置分割成 left 和 right 两部分
        for (let j = 0; j < curWord.length; j++) {
            const left = curWord.substring(0, j);
            const right = curWord.substring(j);

            // 情况一：left 是回文，寻找 reverse(right) 放在前面
            // 拼接结果：reverse(right) + left + right
            //          ↑                      ↑
            //          └──── 互为镜像，抵消 ────┘
            // 中间的 left 是回文，所以整体是回文
            if (
                isPalindrome(left) &&
                reverseds.has(right) &&
                reverseds.get(right) !== i
            ) {
                res.push([reverseds.get(right), i]);
            }

            // 情况二：right 是回文，寻找 reverse(left) 放在后面
            // 拼接结果：left + right + reverse(left)
            //           ↑                    ↑
            //           └──── 互为镜像，抵消 ────┘
            // 中间的 right 是回文，所以整体是回文
            if (
                isPalindrome(right) &&
                reverseds.has(left) &&
                reverseds.get(left) !== i
            ) {
                res.push([i, reverseds.get(left)]);
            }
        }
    }

    return res;
};
